RED = (255, 0, 0)

# Set the dimensions of the screen
SCREEN_WIDTH = 640
SCREEN_HEIGHT = 480

# Set the size of the grid
GRID_SIZE = 20
GRID_WIDTH = SCREEN_WIDTH // GRID_SIZE
GRID_HEIGHT = SCREEN_HEIGHT // GRID_SIZE

# Set the initial snake speed
